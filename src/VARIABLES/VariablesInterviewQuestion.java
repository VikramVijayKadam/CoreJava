package VARIABLES;

public class VariablesInterviewQuestion 
{

	public static void main(String[] args) 
	{
		System.out.println("10\n20\n30\n"); //prints 10 next line 20 next line 30 next line
		System.out.println("10\t20\t30\t"); // prints 10 tab 20 tab 30 tab
		System.out.println();               // brings cursor to next line
		
		//write a program to print -> Print:"Hello World"
		
		System.out.println("Print:\"Hello World\"");
		System.out.println("\\n");       //prints \n
		
	}

}
