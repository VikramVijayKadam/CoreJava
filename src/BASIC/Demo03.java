package BASIC;

public class Demo03 
{
	public static void main(String[] args) 
	{
		int i = 10;
		
		System.out.println("i"); // prints string --> i
		System.out.println('i'); //prints character --> i
		System.out.println(i);  //prints value of i (Variable)
		
		System.out.println(i=20); // we can change the value of variable any no. of times during execution
	}
}
